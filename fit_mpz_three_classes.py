    requested = [300.0, 700.0, 1100.0]
    selected = [min(available_T, key=lambda x: abs(x - t)) for t in requested]
    selected = list(dict.fromkeys(selected))
    fig, axes = plt.subplots(len(classes), len(selected),
                             figsize=(4.2 * len(selected), 3.2 * len(classes)),
                             squeeze=False)
    for i, klass in enumerate(classes):
        for j, T in enumerate(selected):
            e = events[(events.target_class == klass) & (events.T_K == T)]
            ax = axes[i, j]
            ax.plot(e.a_um, e.K_MPa_sqrt_m, "o-", ms=3)
            ax.set_title(f"{klass}, {T:g} K")
            ax.set_xlabel("Crack extension [μm]")
            ax.set_ylabel("K event [MPa√m]")
            ax.grid(True, alpha=0.25)
    fig.tight_layout()
    fig.savefig(out / "three_class_representative_Rcurves.png", dpi=180)
    plt.close(fig)


