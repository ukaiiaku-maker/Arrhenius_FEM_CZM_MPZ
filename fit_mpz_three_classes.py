def fs(x: Any) -> str:
    return f"{float(x):.16g}"


def parse_float_pair(text: str) -> tuple[float, float]:
    vals = [float(x) for x in str(text).replace(",", " ").split() if x]
    if len(vals) != 2 or vals[1] <= vals[0]:
        raise argparse.ArgumentTypeError("expected two increasing values")
    return vals[0], vals[1]


def parse_float_list(text: str) -> list[float]:
    return [float(x) for x in str(text).replace(",", " ").split() if x]


