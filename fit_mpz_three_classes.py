@dataclass(frozen=True)
class ParameterSpec:
    vector_name: str
    row_name: str
    lower: float
    upper: float
    transform: str = "linear"


SPEC_CLEAVE_G = ParameterSpec("cleave_G00_eV", "cleave_G00_eV", 0.6, 4.5)
SPEC_CLEAVE_GT = ParameterSpec("cleave_gT_eV_per_K", "cleave_gT_eV_per_K", -0.0015, 0.0025)
SPEC_CLEAVE_SIG = ParameterSpec("cleave_sigc0_GPa", "cleave_sigc0_GPa", 1.5, 8.0)
SPEC_EMIT_G = ParameterSpec("emit_G00_eV", "emit_G00_eV", 0.6, 3.5)
