def flatten_metrics(klass: str, sims: list[dict[str, Any]]) -> tuple[list[dict[str, Any]], list[dict[str, Any]]]:
    metrics: list[dict[str, Any]] = []
    events: list[dict[str, Any]] = []
    for q in sims:
        rec = {k: v for k, v in q.items() if k != "events"}
        metrics.append({"target_class": klass, **rec})
        for ev in q["events"]:
            events.append({"target_class": klass, "T_K": q["T_K"], **ev})
    return metrics, events


def save_summary_plots(out: Path, metrics: pd.DataFrame, events: pd.DataFrame,
                       targets: pd.DataFrame) -> None:
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except Exception:
        return

    classes = [x for x in ("ceramic", "weakT", "DBTT") if x in set(metrics.target_class)]
