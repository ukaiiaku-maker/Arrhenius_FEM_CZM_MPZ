FIRST_SPECS = [SPEC_CLEAVE_G, SPEC_CLEAVE_GT, SPEC_CLEAVE_SIG,
               SPEC_EMIT_G, SPEC_EMIT_GT, SPEC_EMIT_SIG]
RCURVE_SPECS = [SPEC_SITES, SPEC_GLIDE, SPEC_TRAP, SPEC_DETRAP,
                SPEC_RETREC, SPEC_CBLUNT, SPEC_SRCREC, SPEC_REFRESH,
                SPEC_LENGTH]
REDUCED_SPECS = [SPEC_CLEAVE_G, SPEC_CLEAVE_GT, SPEC_EMIT_G, SPEC_EMIT_GT,
                 SPEC_SITES, SPEC_GLIDE, SPEC_TRAP, SPEC_DETRAP,
                 SPEC_RETREC, SPEC_CBLUNT]
FULL_SPECS = FIRST_SPECS + RCURVE_SPECS


def parameter_specs(profile: str) -> list[ParameterSpec]:
    if profile == "first":
        return list(FIRST_SPECS)
    if profile == "rcurve":
        return list(RCURVE_SPECS)
    if profile == "reduced":
        return list(REDUCED_SPECS)
    if profile == "full":
        return list(FULL_SPECS)
    raise ValueError(profile)


def row_to_vector(row: pd.Series, specs: list[ParameterSpec]) -> np.ndarray:
    vals = []
    for spec in specs:
        value = float(row[spec.row_name])
        vals.append(math.log(max(value, 1.0e-300)) if spec.transform == "log" else value)
    return np.asarray(vals, dtype=float)


def vector_to_row(base: pd.Series, x: np.ndarray,
                  specs: list[ParameterSpec]) -> pd.Series:
    row = base.copy()
    for value, spec in zip(np.asarray(x, dtype=float), specs):
        row[spec.row_name] = math.exp(float(value)) if spec.transform == "log" else float(value)
    # High-stress cleavage entropy is deliberately not a class-fit knob in v9.1.
    row["cleave_S_hs_kB"] = 0.0
    return row


