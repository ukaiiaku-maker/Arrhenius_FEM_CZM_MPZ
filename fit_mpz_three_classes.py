def pseudo_huber(z: float, delta: float = 1.0) -> float:
    if not np.isfinite(z):
        return 25.0
    # Cap a single pathological state from numerically overwhelming every
    # other physical target while preserving a strong rejection penalty.
    z = float(np.clip(z, -100.0 * delta, 100.0 * delta))
    return float(delta * delta * (math.sqrt(1.0 + (z / delta) ** 2) - 1.0))


def interval_loss(value: float, low: float, high: float, scale: float = 1.0) -> float:
    if not np.isfinite(value):
        return 25.0
    if value < low:
        return pseudo_huber((value - low) / max(scale, 1.0e-12))
    if value > high:
        return pseudo_huber((value - high) / max(scale, 1.0e-12))
    return 0.0


def monotonic_violation(values: np.ndarray, *, increasing: bool,
                        tolerance: float = 0.25) -> float:
    v = np.asarray(values, dtype=float)
    v = v[np.isfinite(v)]
    if v.size < 2:
        return 10.0
    d = np.diff(v)
    bad = np.maximum((-d if increasing else d) - tolerance, 0.0)
    return float(np.sum(np.square(bad / max(tolerance, 0.25))))


def _plastic_indicator(q: dict[str, Any], prefix: str) -> float:
    """Compact finite indicator of shielding/blunting at initiation or plateau."""
    shield = float(q.get(f"{prefix}_shield_fraction", float("nan")))
    blunt = float(q.get(f"{prefix}_blunt_ratio", float("nan")))
    candidates = []
    if np.isfinite(shield):
        candidates.append(max(shield, 0.0))
    if np.isfinite(blunt):
        candidates.append(max(0.15 * (blunt - 1.0), 0.0))
    return max(candidates) if candidates else float("nan")


def class_constraint_loss(klass: str, sims: list[dict[str, Any]], mode: str = "joint") -> tuple[float, dict[str, float]]:
    ordered = sorted(sims, key=lambda q: q["T_K"])
    T = np.asarray([q["T_K"] for q in ordered], dtype=float)
    Ki = np.asarray([q["K_init"] for q in ordered], dtype=float)
    Kp = np.asarray([q["K_plateau"] for q in ordered], dtype=float)
    dR = np.asarray([q["delta_KR"] for q in ordered], dtype=float)

    use_first = mode in ("first", "joint")
    use_rcurve = mode in ("rcurve", "joint")
    parts: dict[str, float] = {}
    if klass == "ceramic":
        if use_first:
            parts["first_temperature_order"] = monotonic_violation(Ki, increasing=False)
        if use_rcurve:
            parts["plateau_temperature_order"] = monotonic_violation(Kp, increasing=False)
            parts["brittle_Rcurve"] = float(np.nansum([
                interval_loss(v, 0.0, 1.5, 0.5) for v in dR
            ]))
    elif klass == "weakT":
        if use_first:
            finite_i = Ki[np.isfinite(Ki)]
            parts["first_passage_flatness"] = interval_loss(
                float(np.ptp(finite_i)) if finite_i.size else float("nan"),
                0.0, 2.5, 0.75,
            )
        if use_rcurve:
            finite_p = Kp[np.isfinite(Kp)]
            parts["plateau_flatness"] = interval_loss(
                float(np.ptp(finite_p)) if finite_p.size else float("nan"),
                0.0, 3.0, 1.0,
            )
            parts["moderate_Rcurve"] = float(np.nansum([
                interval_loss(v, 3.0, 7.0, 1.0) for v in dR
            ]))
    elif klass == "DBTT":
        low = T <= 500.0
        high = T >= 900.0
        low_Ki = float(np.nanmedian(Ki[low])) if np.any(low) else float("nan")
        high_Ki = float(np.nanmedian(Ki[high])) if np.any(high) else float("nan")
        low_Kp = high_Kp = low_dR = high_dR = float("nan")
        if use_rcurve:
            low_Kp = float(np.nanmedian(Kp[low])) if np.any(low) else float("nan")
            high_Kp = float(np.nanmedian(Kp[high])) if np.any(high) else float("nan")
            low_dR = float(np.nanmedian(dR[low])) if np.any(low) else float("nan")
            high_dR = float(np.nanmedian(dR[high])) if np.any(high) else float("nan")
        if use_first:
            parts["first_temperature_order"] = monotonic_violation(
                Ki, increasing=True, tolerance=0.75
            )
            parts["transition_first_passage"] = interval_loss(
                high_Ki - low_Ki, 18.0, 40.0, 4.0
            )
            # A cleavage-only thermal slope is not sufficient for the DBTT
            # class.  Plastic shielding/blunting must be weak on the low-T
            # shelf and measurably developed before the high-T first event.
            low_init_plastic = np.nanmedian([
                _plastic_indicator(q, "init") for q in ordered
                if q["T_K"] <= 500.0
            ])
            high_init_plastic = np.nanmedian([
                _plastic_indicator(q, "init") for q in ordered
                if q["T_K"] >= 900.0
            ])
            parts["first_passage_microstructure_switch"] = (
                interval_loss(float(low_init_plastic), 0.0, 0.12, 0.04)
                + interval_loss(float(high_init_plastic), 0.06, 0.80, 0.05)
            )
        if use_rcurve:
            parts["plateau_temperature_order"] = monotonic_violation(
                Kp, increasing=True, tolerance=1.0
            )
            parts["transition_plateau"] = interval_loss(
                high_Kp - low_Kp, 25.0, 50.0, 5.0
            )
            parts["lowT_brittleness"] = interval_loss(low_dR, 0.0, 1.5, 0.5)
            parts["highT_rising_resistance"] = interval_loss(
                high_dR, 7.0, 14.0, 2.0
            )

            # The DBTT high-temperature branch must be connected to evolved
            # shielding/blunting, not only to a direct cleavage-barrier slope.
            low_sims = [q for q in ordered if q["T_K"] <= 500.0]
            high_sims = [q for q in ordered if q["T_K"] >= 900.0]
            low_plastic = np.nanmedian([
                _plastic_indicator(q, "plateau") for q in low_sims
            ]) if low_sims else float("nan")
            high_plastic = np.nanmedian([
                _plastic_indicator(q, "plateau") for q in high_sims
            ]) if high_sims else float("nan")
            parts["microstructure_switch"] = (
                interval_loss(float(low_plastic), 0.0, 0.15, 0.05)
                + interval_loss(float(high_plastic), 0.12, 0.80, 0.08)
            )
    else:
        raise ValueError(f"unknown class: {klass}")
    return float(sum(parts.values())), parts


def state_regularization_loss(sims: list[dict[str, Any]], row: pd.Series) -> tuple[float, dict[str, float]]:
    shield = 0.0
    sites = 0.0
    geometry = 0.0
    incomplete = 0.0
    for q in sims:
        shield += interval_loss(float(q["max_shield_fraction"]), 0.0, 0.90, 0.05)
        shield += interval_loss(float(q.get("max_K_shield_MPa_sqrt_m", 0.0)),
                                0.0, 100.0, 10.0)
        # Exact source exhaustion at essentially every crack position behaves
        # like a hidden threshold.  Small depletion is allowed and expected.
        sites += interval_loss(float(q["min_site_fraction"]), 0.005, 1.0, 0.01)
        r_ratio_limit = max(float(row.mpz_length_m) / max(float(row.r_pz_m), 1.0e-30), 2.0)
        geometry += interval_loss(float(q["max_blunt_ratio"]), 1.0, r_ratio_limit, 2.0)
        incomplete += 20.0 * (1.0 - float(q["event_fraction"])) ** 2
    parts = {"shielding_bound": shield, "source_inventory": sites,
             "blunting_geometry": geometry, "incomplete_growth": incomplete}
    return float(sum(parts.values())), parts


def point_target_loss(targets: pd.DataFrame,
                      sims: list[dict[str, Any]],
                      mode: str = "joint") -> tuple[float, list[dict[str, Any]]]:
    by_T = {float(q["T_K"]): q for q in sims}
    total = 0.0
    details: list[dict[str, Any]] = []
    use_first = mode in ("first", "joint")
    use_rcurve = mode in ("rcurve", "joint")
    for _, t in targets.iterrows():
        q = by_T.get(float(t.T_K))
        if q is None:
            total += 100.0
            continue
        weight = float(t.get("weight", 1.0))
        components: dict[str, float] = {}
        if use_first:
            components["K_init"] = pseudo_huber(
                (q["K_init"] - t.K_init_target) / t.K_init_scale
            )
        if use_rcurve:
            components.update({
                "K_plateau": pseudo_huber(
                    (q["K_plateau"] - t.K_plateau_target) / t.K_plateau_scale
                ),
                "early_slope": pseudo_huber(
                    (q["early_rise_per_100um"] - t.early_rise_per_100um_target)
                    / t.early_rise_scale
                ),
                "plateau_slope": pseudo_huber(
                    (q["plateau_rise_per_100um"] - t.plateau_rise_per_100um_target)
                    / t.plateau_rise_scale
                ),
                "delta_KR_range": interval_loss(
                    q["delta_KR"], float(t.delta_KR_min),
                    float(t.delta_KR_max), 1.0
                ),
            })
        score = weight * float(sum(components.values()))
        total += score
        details.append({"T_K": float(t.T_K), "score": score, **components})
    return total, details


def objective_for_class(base: pd.Series, targets: pd.DataFrame, klass: str,
                        opt: argparse.Namespace, specs: list[ParameterSpec]):
    Ts = targets.T_K.to_numpy(dtype=float)
    cache: dict[tuple[float, ...], tuple[float, pd.Series, list[dict[str, Any]], dict[str, Any]]] = {}

    def evaluate(x: np.ndarray, details: bool = False):
        key = tuple(np.round(np.asarray(x, dtype=float), 11))
        if key not in cache:
            row = vector_to_row(base, np.asarray(x, dtype=float), specs)
            sims = [simulate(row, T, opt) for T in Ts]
            point, point_rows = point_target_loss(targets, sims, opt.objective_mode)
            class_loss, class_parts = class_constraint_loss(klass, sims, opt.objective_mode)
            state_loss, state_parts = state_regularization_loss(sims, row)
            total = (
                opt.w_points * point + opt.w_class * class_loss
                + opt.w_state * state_loss
            )
            payload = {
                "point": point, "class": class_loss, "state": state_loss,
                "class_parts": class_parts, "state_parts": state_parts,
                "point_rows": point_rows,
            }
            cache[key] = (float(total), row, sims, payload)
        result = cache[key]
        return result if details else result[0]

    return evaluate


