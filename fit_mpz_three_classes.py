SPEC_EMIT_SIG = ParameterSpec("emit_sigc0_GPa", "emit_sigc0_GPa", 0.5, 8.0)
SPEC_SITES = ParameterSpec("log_source_sites", "mpz_source_sites_per_system", math.log(0.5), math.log(300.0), "log")
SPEC_GLIDE = ParameterSpec("mpz_glide_barrier_eV", "mpz_glide_barrier_eV", 0.25, 2.2)
SPEC_TRAP = ParameterSpec("mpz_trap_barrier_eV", "mpz_trap_barrier_eV", 0.25, 2.0)
SPEC_DETRAP = ParameterSpec("mpz_detrap_barrier_eV", "mpz_detrap_barrier_eV", 0.50, 2.8)
SPEC_RETREC = ParameterSpec("mpz_retained_recovery_barrier_eV", "mpz_retained_recovery_barrier_eV", 0.80, 3.2)
