# ---------------------------------------------------------------------------
# Robust R-curve summaries
# ---------------------------------------------------------------------------

def median_pairwise_slope(x: np.ndarray, y: np.ndarray) -> float:
    """Median pairwise slope, robust to event-level R-curve scatter."""
    x = np.asarray(x, dtype=float)
    y = np.asarray(y, dtype=float)
    good = np.isfinite(x) & np.isfinite(y)
    x = x[good]
    y = y[good]
    if x.size < 2:
        return float("nan")
    slopes: list[float] = []
    for i in range(x.size - 1):
        dx = x[i + 1 :] - x[i]
        use = np.abs(dx) > 1.0e-12
        if np.any(use):
            slopes.extend(((y[i + 1 :][use] - y[i]) / dx[use]).tolist())
    return float(np.median(slopes)) if slopes else float("nan")


def window_values(x: np.ndarray, y: np.ndarray,
                  window: tuple[float, float]) -> tuple[np.ndarray, np.ndarray]:
    use = (x >= window[0]) & (x <= window[1]) & np.isfinite(x) & np.isfinite(y)
    return x[use], y[use]


def robust_saturation_fit(a_um: np.ndarray, K: np.ndarray) -> dict[str, float]:
    """Fit K=K0+dK*(1-exp(-(a/L)^p)) with a soft-L1 loss.

    The fit is diagnostic.  The optimization objective uses the more robust
    median plateau and median pairwise slopes as its primary R-curve targets.
    """
    a = np.asarray(a_um, dtype=float)
    y = np.asarray(K, dtype=float)
    good = np.isfinite(a) & np.isfinite(y)
    a = a[good]
    y = y[good]
    if a.size < 8:
        return {"sat_K0": float("nan"), "sat_delta_KR": float("nan"),
                "sat_Kss": float("nan"), "sat_length_um": float("nan"),
                "sat_power": float("nan"), "sat_rmse": float("nan")}

    k0 = float(np.median(y[: min(3, y.size)]))
    kss = float(np.median(y[-min(8, y.size) :]))
    d0 = max(kss - k0, 0.05)
    L0 = max(float(np.median(a[a > 0])) if np.any(a > 0) else 200.0, 20.0)
    p0 = 1.0

    def residual(z: np.ndarray) -> np.ndarray:
        K0, dK, logL, logp = z
        L = math.exp(logL)
        p = math.exp(logp)
        pred = K0 + dK * (1.0 - np.exp(-np.power(np.maximum(a, 0.0) / L, p)))
        return pred - y

    lower = np.array([0.0, 0.0, math.log(2.0), math.log(0.15)])
    upper = np.array([100.0, 100.0, math.log(1.0e5), math.log(6.0)])
    try:
        res = least_squares(
            residual, np.array([k0, d0, math.log(L0), math.log(p0)]),
            bounds=(lower, upper), loss="soft_l1", f_scale=0.5, max_nfev=1000,
        )
        K0, dK, logL, logp = res.x
        rmse = float(np.sqrt(np.mean(np.square(residual(res.x)))))
        return {"sat_K0": float(K0), "sat_delta_KR": float(dK),
                "sat_Kss": float(K0 + dK), "sat_length_um": float(math.exp(logL)),
                "sat_power": float(math.exp(logp)), "sat_rmse": rmse}
    except Exception:
        return {"sat_K0": float("nan"), "sat_delta_KR": float("nan"),
                "sat_Kss": float("nan"), "sat_length_um": float("nan"),
                "sat_power": float("nan"), "sat_rmse": float("nan")}


def summarize_rcurve(a_um: Iterable[float], K: Iterable[float],
                     early_window: tuple[float, float],
                     plateau_window: tuple[float, float]) -> dict[str, float]:
    a = np.asarray(list(a_um), dtype=float)
    y = np.asarray(list(K), dtype=float)
    if y.size == 0:
        base = {
            "K_init": float("nan"), "K_plateau": float("nan"),
            "delta_KR": float("nan"), "early_rise_per_100um": float("nan"),
            "plateau_rise_per_100um": float("nan"), "n_early": 0,
            "n_plateau": 0, "roughness_MAD": float("nan"),
        }
        return {**base, **robust_saturation_fit(a, y)}

    xe, ye = window_values(a, y, early_window)
    xp, yp = window_values(a, y, plateau_window)
    early_slope = median_pairwise_slope(xe, ye)
    plateau_slope = median_pairwise_slope(xp, yp)
    Kplateau = float(np.median(yp)) if yp.size else float("nan")
    Kinit = float(y[0])

    # Robust local roughness relative to a 5-event rolling median.
    if y.size >= 5:
        roll = pd.Series(y).rolling(5, center=True, min_periods=1).median().to_numpy()
        roughness = float(np.median(np.abs(y - roll)))
    else:
        roughness = float(np.median(np.abs(y - np.median(y))))

    base = {
        "K_init": Kinit,
        "K_plateau": Kplateau,
        "delta_KR": Kplateau - Kinit if np.isfinite(Kplateau) else float("nan"),
        "early_rise_per_100um": early_slope * 100.0,
        "plateau_rise_per_100um": plateau_slope * 100.0,
        "n_early": int(ye.size), "n_plateau": int(yp.size),
        "roughness_MAD": roughness,
    }
    return {**base, **robust_saturation_fit(a, y)}


