#!/usr/bin/env python3
"""Fit three moving-process-zone fracture classes against initiation and R-curves.

The fitted classes are:

* ``ceramic``: toughness decreases with temperature and the response remains
  essentially brittle at every temperature;
* ``weakT``: an intermediate, FCC-like toughness with weak temperature
  dependence and a moderate rising resistance curve;
* ``DBTT``: a brittle low-temperature shelf and a tough high-temperature
  branch whose rising resistance is generated by the evolving process zone.

The objective never fits a scalar backstress, scalar emission saturation,
per-step emission cap, or stored-energy cleavage offset.  It compares robust
R-curve summaries (Theil-Sen-like median slope, median developed resistance,
and a diagnostic robust saturation fit) rather than individual noisy events.
"""
