def simulate(row: pd.Series, T: float, opt: argparse.Namespace) -> dict[str, Any]:
    args = row_to_args(
        row, dK=opt.dK, Kdot=opt.Kdot, n_advances=opt.n_advances,
        Kmax=opt.Kmax, da_um=opt.da_um,
    )
    eng = sf.build_engine(args, make_emergent_config().material)

    # Resolve at most one renewal in each accepted adaptive substep.  This is
    # not a physical event cap: the substep is reduced until the predicted
    # cleavage-clock increment is small, and residual B is retained exactly.
    eng.f.max_advances_per_step = 1

    events: list[dict[str, float]] = []
    max_shield_fraction = 0.0  # evaluated at crack-renewal events
    max_transient_shield_fraction = 0.0
    max_K_shield_MPa_sqrt_m = 0.0
    max_blunt_ratio = 1.0
    min_site_fraction = 1.0
    max_retained = 0.0
    max_active = 0.0
    max_emitted_total = 0.0
    r0 = max(float(eng.f.r0), 1.0e-30)
    current_K = 0.0
    n_substeps = 0
    integration_stalled = False

    def commit_info(Kmpa: float, info: dict[str, Any]) -> None:
        nonlocal max_shield_fraction, max_transient_shield_fraction
        nonlocal max_K_shield_MPa_sqrt_m, max_blunt_ratio, min_site_fraction
        nonlocal max_retained, max_active, max_emitted_total

        Ksh_post = float(info.get("mpz_K_shield_Pa_sqrt_m", 0.0)) / 1.0e6
        max_K_shield_MPa_sqrt_m = max(max_K_shield_MPa_sqrt_m, Ksh_post)
        max_transient_shield_fraction = max(
            max_transient_shield_fraction, Ksh_post / max(Kmpa, 1.0e-12)
        )
        max_blunt_ratio = max(max_blunt_ratio, float(info.get("r_eff", r0)) / r0)
        min_site_fraction = min(
            min_site_fraction, float(info.get("mpz_available_site_fraction", 1.0))
        )
        max_retained = max(
            max_retained, float(info.get("mpz_retained_count", info.get("N_em", 0.0)))
        )
        max_active = max(max_active, float(info.get("mpz_active_count", 0.0)))
        max_emitted_total = max(
            max_emitted_total, float(info.get("mpz_emitted_total", 0.0))
        )

        nfire = int(info.get("n_fire", 0))
        if not nfire:
            return
        Ksh_pre = float(
            info.get("mpz_K_shield_pre_renewal_Pa_sqrt_m",
                     info.get("mpz_K_shield_Pa_sqrt_m", 0.0))
        ) / 1.0e6
        rpre = float(info.get("r_eff_pre_renewal", info.get("r_eff", r0)))
        retained_pre = float(
            info.get("mpz_retained_pre_renewal",
                     info.get("N_em_pre_renewal", info.get("N_em", 0.0)))
        )
        mobile_pre = float(info.get("mpz_mobile_pre_renewal", 0.0))
        site_pre = float(
            info.get("mpz_available_site_fraction_pre_renewal",
                     info.get("mpz_available_site_fraction", 1.0))
        )
        slip_pre = float(info.get("mpz_local_slip_pre_renewal", 0.0))
        emitted_pre = float(
            info.get("mpz_emitted_total_pre_renewal",
                     info.get("mpz_emitted_total", 0.0))
        )
        for _ in range(nfire):
            idx = len(events)
            events.append({
                "event_index": float(idx),
                "a_um": float(idx * opt.da_um),
                "K_MPa_sqrt_m": float(Kmpa),
                "K_shield_MPa_sqrt_m": float(Ksh_pre),
                "shield_fraction": float(Ksh_pre / max(Kmpa, 1.0e-12)),
                "blunt_ratio": float(rpre / r0),
                "retained_count": retained_pre,
                "mobile_count": mobile_pre,
                "available_site_fraction": site_pre,
                "local_slip_count": slip_pre,
                "emitted_total": emitted_pre,
            })
            max_shield_fraction = max(
                max_shield_fraction, Ksh_pre / max(Kmpa, 1.0e-12)
            )
            max_blunt_ratio = max(max_blunt_ratio, rpre / r0)
            min_site_fraction = min(min_site_fraction, site_pre)
            max_retained = max(max_retained, retained_pre)
            max_active = max(max_active, retained_pre + mobile_pre)
            max_emitted_total = max(max_emitted_total, emitted_pre)

    tol_K = max(1.0e-12, 1.0e-12 * opt.Kmax)
    while current_K < opt.Kmax - tol_K and len(events) < opt.n_advances:
        nominal_end = min(current_K + opt.dK, opt.Kmax)
        while current_K < nominal_end - tol_K and len(events) < opt.n_advances:
            if n_substeps >= opt.max_substeps:
                integration_stalled = True
                break

            # Consume any residual completed renewal before adding physical time.
            if float(eng.B) >= 1.0:
                info = eng.step(current_K * 1.0e6, T, 0.0)
                n_substeps += 1
                commit_info(current_K, info)
                if not info.get("fired", False):
                    integration_stalled = True
                    break
                continue

            dK_proposed = nominal_end - current_K
            dt_proposed = dK_proposed / opt.Kdot

            cap = max(float(np.sum(eng.mpz_state.site_capacity)), 1.0e-300)
            avail_fraction = float(np.sum(eng.mpz_state.available_sites)) / cap

            def acceptable(fraction_trial: float) -> bool:
                dK_trial = dK_proposed * fraction_trial
                dt_trial = dt_proposed * fraction_trial
                K_trial = current_K + dK_trial
                if opt.target_dB_substep > 0.0:
                    dB_trial = float(
                        eng.predict_clock_increment(K_trial * 1.0e6, T, dt_trial)
                    )
                    if dB_trial > opt.target_dB_substep:
                        return False
                if (opt.target_emission_hazard_substep > 0.0
                        and avail_fraction > opt.source_active_fraction_min):
                    sig_trial = eng.sigma_tip(K_trial * 1.0e6)
                    H_trial = float(eng.lambda_emit(sig_trial, T)[0] * dt_trial)
                    if H_trial > opt.target_emission_hazard_substep:
                        return False
                return True

            if acceptable(1.0):
                fraction = 1.0
            else:
                lo = 0.0
                hi = 1.0
                for _ in range(36):
                    mid = 0.5 * (lo + hi)
                    if acceptable(mid):
                        lo = mid
                    else:
                        hi = mid
                fraction = max(lo, opt.min_substep_fraction)

            fraction = float(np.clip(fraction, opt.min_substep_fraction, 1.0))
            dK_step = max(dK_proposed * fraction, tol_K)
            dK_step = min(dK_step, dK_proposed)
            dt_step = dK_step / opt.Kdot
            current_K += dK_step
            info = eng.step(current_K * 1.0e6, T, dt_step)
            n_substeps += 1
            commit_info(current_K, info)

        if integration_stalled:
            break

    event_df = pd.DataFrame(events)
    metrics = summarize_rcurve(
        event_df.get("a_um", pd.Series(dtype=float)).to_numpy(),
        event_df.get("K_MPa_sqrt_m", pd.Series(dtype=float)).to_numpy(),
        opt.early_window_um, opt.plateau_window_um,
    )

    def event_median(column: str, window: tuple[float, float]) -> float:
        if event_df.empty or column not in event_df:
            return float("nan")
        use = (event_df.a_um >= window[0]) & (event_df.a_um <= window[1])
        return float(event_df.loc[use, column].median()) if use.any() else float("nan")

    metrics.update({
        "T_K": float(T),
        "n_events": int(len(events)),
        "event_fraction": float(min(len(events) / max(opt.n_advances, 1), 1.0)),
        "completed": bool(len(events) >= opt.n_advances),
        "integration_stalled": bool(integration_stalled),
        "n_substeps": int(n_substeps),
        "max_shield_fraction": float(max_shield_fraction),
        "max_transient_shield_fraction": float(max_transient_shield_fraction),
        "max_K_shield_MPa_sqrt_m": float(max_K_shield_MPa_sqrt_m),
        "max_blunt_ratio": float(max_blunt_ratio),
        "min_site_fraction": float(min_site_fraction),
        "max_retained": float(max_retained),
        "max_active": float(max_active),
        "max_emitted_total": float(max_emitted_total),
        "init_shield_fraction": float(event_df.iloc[0].shield_fraction) if len(event_df) else float("nan"),
        "plateau_shield_fraction": event_median("shield_fraction", opt.plateau_window_um),
        "init_blunt_ratio": float(event_df.iloc[0].blunt_ratio) if len(event_df) else float("nan"),
        "plateau_blunt_ratio": event_median("blunt_ratio", opt.plateau_window_um),
        "init_site_fraction": float(event_df.iloc[0].available_site_fraction) if len(event_df) else float("nan"),
        "plateau_site_fraction": event_median("available_site_fraction", opt.plateau_window_um),
        "plateau_retained_count": event_median("retained_count", opt.plateau_window_um),
        "events": events,
    })
    return metrics


